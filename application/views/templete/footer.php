 $(document).ready(function() {
    
    $('#contact-form').submit(function(event) {
        event.preventDefault();
        
        var formData = {
            'name': $('#name').val(),
            'email': $('#email').val(),
            'message': $('#message').val(),
            'mobile': $('#number').val()
        };
        
        $.ajax({
            type: 'POST',
            url: '<?php echo site_url("Login/submit"); ?>', // Form action URL
            data: formData,
            dataType: 'json',
            encode: true
        })
        .done(function(response) {
            if (response.success) {
                $('#contact-form').append('<h3 class="text-success">Message sent successfully!</h3>');
                setTimeout(function() {
                    $('.text-success').fadeOut('fast');
                }, 1000);
                $('#contact-form')[0].reset();
            } else {
                $('#name-error').html(response.errors.name);
                $('#email-error').html(response.errors.email);
                $('#message-error').html(response.errors.message);
                $('#mobile-error').html(response.errors.mobile);
            }
        })
        .fail(function() {
            alert('Failed to send message. Please try again later.');
        });
    });
});

      function validate(phoneNum) {
     
